public class Solution563 {
    /**
     * Definition for a binary tree node.
     * | public class TreeNode {
     * |     int val;
     * |     TreeNode left;
     * |     TreeNode right;
     * |     TreeNode() {}
     * |     TreeNode(int val) { this.val = val; }
     * |     TreeNode(int val, TreeNode left, TreeNode right) {
     * |         this.val = val;
     * |         this.left = left;
     * |         this.right = right;
     * |     }
     * | }
     */
    /**
     * 执行用时： 1 ms , 在所有 Java 提交中击败了 30.55% 的用户
     * 内存消耗： 38.7 MB , 在所有 Java 提交中击败了 13.07% 的用户
     * 通过测试用例： 77 / 77
     *
     * @param root
     * @return
     */
    public int findTilt(TreeNode root) {
        return findSumAndTiltSum(root)[1];
    }

    private int[] findSumAndTiltSum(TreeNode root) {
        if (root == null) {
            return new int[]{0, 0, 0};
        }
        int[] left = findSumAndTiltSum(root.left);
        int[] right = findSumAndTiltSum(root.right);
        return new int[]{left[0] + right[0] + root.val, right[1] + left[1] + Math.abs(right[0] - left[0])};
    }

    static class TreeNode {
        int val;
        TreeNode left;
        TreeNode right;

        TreeNode() {
        }

        TreeNode(int val) {
            this.val = val;
        }

        TreeNode(int val, TreeNode left, TreeNode right) {
            this.val = val;
            this.left = left;
            this.right = right;
        }
    }
}
