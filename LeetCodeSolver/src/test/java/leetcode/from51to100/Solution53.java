public class Solution53 {
    /**
     * 执行用时： 1 ms , 在所有 Java 提交中击败了 100.00% 的用户
     * 内存消耗： 48.5 MB , 在所有 Java 提交中击败了 54.82% 的用户
     * 通过测试用例： 209 / 209
     *
     * @param nums
     * @return
     */
    public int maxSubArray(int[] nums) {
        int sum = 0;
        int max = Integer.MIN_VALUE;
        for (int i = 0; i < nums.length; i++) {
            if (nums[i] > max) {
                max = nums[i];
            }
            if (sum + nums[i] < 0) {
                sum = 0;
            } else {
                sum += nums[i];
                if (sum > max) {
                    max = sum;
                }
            }
        }
        return max;
    }
}
