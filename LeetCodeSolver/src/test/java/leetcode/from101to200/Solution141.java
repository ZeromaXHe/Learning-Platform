public class Solution141 {
    /**
     * Definition for singly-linked list.
     * | class ListNode {
     * |     int val;
     * |     ListNode next;
     * |     ListNode(int x) {
     * |         val = x;
     * |         next = null;
     * |     }
     * | }
     * <p>
     * 执行用时： 0 ms , 在所有 Java 提交中击败了 100.00% 的用户
     * 内存消耗： 39.5 MB , 在所有 Java 提交中击败了 56.50% 的用户
     * 通过测试用例： 21 / 21
     *
     * @param head
     * @return
     */
    public boolean hasCycle(ListNode head) {
        if (head == null || head.next == null) {
            return false;
        }
        ListNode pre = head;
        ListNode post = head.next;
        while (post != null) {
            post = post.next;
            if (post == null) {
                break;
            }
            post = post.next;
            pre = pre.next;
            if (pre == post) {
                return true;
            }
        }
        return false;
    }

    static class ListNode {
        int val;
        ListNode next;

        ListNode(int x) {
            val = x;
            next = null;
        }
    }
}
