public class Solution70 {
    /**
     * 执行用时： 0 ms , 在所有 Java 提交中击败了 100.00% 的用户
     * 内存消耗： 35.3 MB , 在所有 Java 提交中击败了 8.84% 的用户
     * 通过测试用例： 45 / 45
     *
     * @param n
     * @return
     */
    public int climbStairs(int n) {
        if (n == 1) {
            return 1;
        }
        if (n == 2) {
            return 2;
        }
        int fib1 = 1;
        int fib2 = 2;
        n -= 2;
        while (n > 0) {
            int temp = fib1 + fib2;
            fib1 = fib2;
            fib2 = temp;
            n--;
        }
        return fib2;
    }
}
